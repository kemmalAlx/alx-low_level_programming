#include "3-calc.h"

/**
 * op_add - function add
 *
 * @a: first value
 * @b: second value
 *
 * Return: add a + b
*/
int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - function add
 *
 * @a: first value
 * @b: second value
 *
 * Return: add a - b
*/
int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - function add
 *
 * @a: first value
 * @b: second value
 *
 * Return: add a * b
*/
int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - function add
 *
 * @a: first value
 * @b: second value
 *
 * Return: add a / b
*/
int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - function add
 *
 * @a: first value
 * @b: second value
 *
 * Return: add a % b
*/
int op_mod(int a, int b)
{
	return (a % b);
}
